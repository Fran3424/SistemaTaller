    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
           BLEmpleados logica = new BLEmpleados();
        EntidadEmpleado empleado = GenerarEntidad();
        try {
            if (empleado.isExiste()) {
                logica.Modificar(empleado);
            }
            else{
                logica.Insertar(empleado);
            }
            Limpiar();
            CargarDatos("");
            JOptionPane.showMessageDialog(this, logica.getMensaje());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "ERROR " + ex.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnElliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElliminarActionPerformed
            BLEmpleados logica = new BLEmpleados();
        EntidadEmpleado empleado;
        try {
            empleado = GenerarEntidad();
            if(empleado.isExiste()){
                if(logica.Eliminar(empleado) > 0){
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                    Limpiar();
                    CargarDatos("");
                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el cliente");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar el cliente que desea eliminar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnElliminarActionPerformed

