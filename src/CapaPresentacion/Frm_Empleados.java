      private EntidadEmpleado GenerarEntidad(){
        EntidadEmpleado empleado = new EntidadEmpleado();
        if (!txtId.getText().equals("")) {
            empleado.setExiste(true);
            empleado.setId(Integer.parseInt(txtId.getText()));
        }
        empleado.setNombre(txtNombre.getText());
        empleado.setApellido1(txtApellido1.getText());
         empleado.setApellido2(txtApellido2.getText());
         empleado.setTelefono( Integer.parseInt(txtTelefono.getText()));
        return empleado;
    }
     
     
     
    public void Limpiar() {
        txtId.setText(null);
        txtNombre.setText(null);
        txtTelefono.setText(null);
        txtApellido1.setText(null);
        txtApellido2.setText(null);
    }
     
    private void LimpiarTabla(){
        modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };     
        tblEmpleados.setModel(modelo);
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido1");
        modelo.addColumn("Apellido2");
        modelo.addColumn("Telefono");
        
    }
    
    
    
    
    
  private void CargarDatos(String condicion) throws Exception {
        BLEmpleados logica = new BLEmpleados();
        List<EntidadEmpleado> lista;
        LimpiarTabla();
        Object[] fila = new Object[5];
        try {
            lista = logica.ListarEmpleados(condicion);
            for (EntidadPersona cli : lista) {
                fila[0] = cli.getId();
                fila[1] = cli.getNombre();
                fila[2] = cli.getApellido1();
                fila[3] = cli.getApellido2();
                fila[4] = cli.getTelefono();
                modelo.addRow(fila);
            }
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
