 private EntidadCliente GenerarEntidad(){
        EntidadCliente cliente = new EntidadCliente();
        if (!txtId.getText().equals("")) {
            cliente.setExiste(true);
            cliente.setId(Integer.parseInt(txtId.getText()));
        }
        cliente.setNombre(txtNombre.getText());
        cliente.setApellido1(txtApellido1.getText());
         cliente.setApellido2(txtApellido2.getText());
         cliente.setTelefono( Integer.parseInt(txtTelefono.getText()));
        return cliente;
    }

 
