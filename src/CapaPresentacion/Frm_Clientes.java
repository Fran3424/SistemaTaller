    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
         BLClientes logica = new BLClientes();
         boolean numerico;
          if(!txtNombre.getText().isEmpty()&
              !txtApellido1.getText().isEmpty()&    
             !txtApellido2.getText().isEmpty()&  
             !txtTelefono.getText().isEmpty() &
              esnumero(txtTelefono.getText())
                   ){
               EntidadCliente cliente = GenerarEntidad();
              
         
                try {
                        if (cliente.isExiste()) {
                        logica.Modificar(cliente);
                    }
                else    {
                     logica.Insertar(cliente);
                         }
                     Limpiar();
                     CargarDatos("");
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                     } catch (Exception ex) {
                          JOptionPane.showMessageDialog(this, "ERROR " + ex.getMessage());
                        }
           
              
          } else{
               if(!esnumero(txtTelefono.getText())&
              txtNombre.getText().isEmpty()&
              txtApellido1.getText().isEmpty()&    
             txtApellido2.getText().isEmpty()&  
             txtTelefono.getText().isEmpty()    
               ){
                   JOptionPane.showMessageDialog(this,"Faltan campos por llenar");
               
               }
               
               else{
                JOptionPane.showMessageDialog(this,"Escriba un valor numerico en el telefono");
               
               }
               
               
          }
          
          
         
        
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
          BLClientes logica = new BLClientes();
        EntidadCliente cliente;
        String condicion;
        try {
            if (evt.getClickCount() == 2) {
                int fila = tblClientes.rowAtPoint(evt.getPoint());
                txtId.setText(tblClientes.getValueAt(fila, 0).toString());
              txtNombre.setText(tblClientes.getValueAt(fila, 1).toString());
              txtApellido1.setText(tblClientes.getValueAt(fila, 2).toString());
              txtApellido2.setText(tblClientes.getValueAt(fila, 3).toString());
              txtTelefono.setText(tblClientes.getValueAt(fila, 4).toString());
                condicion = String.format("Id_cliente=%s", txtId.getText());
                cliente = logica.ObtenerUnCliente(condicion);
                txtNombre.setText(cliente.getNombre());
                txtApellido1.setText(cliente.getApellido1());
                txtApellido2.setText(cliente.getApellido2());
                txtTelefono.setText(String.valueOf(cliente.getTelefono()));
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_tblClientesMouseClicked

