    private void btnElliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElliminarActionPerformed
        BLClientes logica = new BLClientes();
        EntidadCliente cliente;
        
        try {
            cliente = GenerarEntidad();
            if(cliente.isExiste()){
                if(logica.Eliminar(cliente) > 0){
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                    Limpiar();
                    CargarDatos("");
                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el cliente");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar el cliente que desea eliminar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        
    }//GEN-LAST:event_btnElliminarActionPerformed

    
     public static boolean esnumero(String str) {
        return str != null && str.matches("[-+]?\\d*\\.?\\d+");
    }
    
    
    
    
    
    
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
      Frm_Buscar_Cliente buscar = new Frm_Buscar_Cliente(null, true);
        buscar.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                try {
                    int id = buscar.ObtenerId();
                    String condicion = "";
                    BLClientes logica = new BLClientes();
                    EntidadCliente cliente;
                    if (id > -1) {
                        condicion = String.format("ID_CLIENTE=%d", id);
                        cliente = logica.ObtenerUnCliente(condicion);
                        txtId.setText(String.valueOf(cliente.getId()));
                        txtNombre.setText(cliente.getNombre());
                       txtTelefono.setText(String.valueOf(cliente.getTelefono()));
                       txtApellido1.setText(cliente.getApellido1());
                       txtApellido2.setText(cliente.getApellido2());
                    }
                    else{
                        txtId.setText("");
                        txtNombre.setText("");
                        txtTelefono.setText("");
                      txtApellido1.setText("");
                      txtApellido2.setText("");
                    }                      
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        });
        buscar.setVisible(true);   
    }//GEN-LAST:event_btnBuscarActionPerformed

